<P>
<A HREF="http://www.FreshPorts.org/">FreshPorts</A> contains the most
up to date information about your favourite ports.  Fully automated, 
it notifies you if any of your ports are updated and emails you with 
the description of the change. It is ideal for both hobbyist and profesional
alike.  With so many changes underway, it's good to know FreshPorts will
keep track and give you one less thing to worry about.
</P>

<P>
Over the past year, the <A HREF="team.html">FreshPorts team</A> has been 
working to bring you the next phase, FreshPorts2.  With this new version
of the website you will be able to watch a particular port category in 
case any new ports are added.  We also have a tool which takes a list
of your installed ports and registers them with FreshPorts.
</P>

<P>
If you'd like to get involved with this project, we urge you to have a look
at the existing list of tasks in <A HREF="/mantis/">Mantis</A> (login a 
guest/guest).  If you see something you'd like to try, let us know.
Failing that, just join the developer's mailing list and tell us what
type of work you'd like to do.
</P>

